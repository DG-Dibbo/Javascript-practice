// do while loop example

// allmost similar to while loop but the difference is that
// in do-while loop the block of code will be executed at least once
// even if the condition is false
let count = 1;
do {
    console.log("Count is:", count);
    count++;
} while (count <= 5);

// Output:
// Count is: 1
// Count is: 2
// Count is: 3
// Count is: 4
// Count is: 5